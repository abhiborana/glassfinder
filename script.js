function exactSearch(exactInput) {
    let exactList = "";
    for (let a = 0; a < lists.length; a++) {
        if (lists[a].includes(exactInput.toLowerCase())) {
            exactList = lists[a].toString().toUpperCase();
            return exactList;
        }
    }
    if (!exactList) {
        return false;
    }
}

